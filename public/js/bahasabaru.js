let currentLang =
    localStorage.getItem("lang") || "id";


// =========================
// DOM LOADED
// =========================
document.addEventListener("DOMContentLoaded", () => {

    applyLanguage(currentLang);

});


// =========================
// TOGGLE LANGUAGE
// =========================
function toggleLanguage(event) {

    event.preventDefault();

    currentLang =
        currentLang === "id"
        ? "en"
        : "id";

    localStorage.setItem(
        "lang",
        currentLang
    );

    applyLanguage(currentLang);
}


// =========================
// APPLY LANGUAGE
// =========================
function applyLanguage(lang) {

    const elements =
        document.querySelectorAll("[data-id]");

    elements.forEach(el => {

        const key =
            el.getAttribute("data-id");

        if (!translations[lang][key]) return;

        // INPUT / TEXTAREA
        if (
            el.tagName === "INPUT" ||
            el.tagName === "TEXTAREA"
        ) {

            el.placeholder =
                translations[lang][key];

        }

        // TEXT
        else {

            el.textContent =
                translations[lang][key];

        }

    });

    // TRANSLATE DATABASE
    translateDatabase(lang);

    // UPDATE FLAG
    updateLanguageUI(lang);

}


// =========================
// UPDATE FLAG & LABEL
// =========================
function updateLanguageUI(lang) {

    const flag =
        document.getElementById("flagIcon");

    const label =
        document.getElementById("langLabel");

    // CEGAH ERROR
    if (!flag || !label) return;

    if (lang === "id") {

        flag.src =
            "https://flagcdn.com/w20/id.png";

        label.textContent =
            "ID-EN";

    } else {

        flag.src =
            "https://flagcdn.com/w20/gb.png";

        label.textContent =
            "EN-ID";

    }

}


// =========================
// LOADING
// =========================
function showLoading(lang) {

    const loading =
        document.getElementById("loadingTranslate");

    if (!loading) return;

    const text =
        loading.querySelector("p");

    if (text) {

        text.textContent =
            lang === "id"
            ? "Menerjemahkan..."
            : "Translating...";

    }

    loading.classList.add("active");

}

function hideLoading() {

    const loading =
        document.getElementById("loadingTranslate");

    if (!loading) return;

    loading.classList.remove("active");

}


// =========================
// CACHE
// =========================
function getCache() {

    return JSON.parse(
        localStorage.getItem("translateCache")
    ) || {};

}

function setCache(cache) {

    localStorage.setItem(
        "translateCache",
        JSON.stringify(cache)
    );

}


// =========================
// TRANSLATE DATABASE
// =========================
function translateDatabase(lang) {

    showLoading(lang);

    let elements =
        document.querySelectorAll("[data-translate]");

    let texts = {};

    let cache = getCache();

    if (!cache[lang]) {

        cache[lang] = {};

    }

    let needTranslate = {};

    elements.forEach(el => {

        let key =
            el.getAttribute("data-key");

        let originalText =
            el.getAttribute("data-original");

        // PRIORITAS KE INDONESIA
        if (lang === "id") {

            el.textContent =
                originalText;

        }

        // CEK CACHE
        else if (
            cache[lang] &&
            cache[lang][key]
        ) {

            el.textContent =
                cache[lang][key];

        }

        // PERLU TRANSLATE
        else {

            texts[key] =
                originalText;

            needTranslate[key] = el;

        }

    });

    // TIDAK ADA YANG DITERJEMAHKAN
    if (Object.keys(texts).length === 0) {

        setTimeout(() => {

            hideLoading();

        }, 200);

        return;

    }

    // FETCH TRANSLATE
    fetch("/translate", {

        method: "POST",

        headers: {

            "Content-Type":
                "application/json",

            "X-CSRF-TOKEN":
                document.querySelector(
                    'meta[name="csrf-token"]'
                ).content

        },

        body: JSON.stringify({

            texts: texts,
            lang: lang

        })

    })

    .then(res => res.json())

    .then(data => {

        Object.keys(data).forEach(key => {

            if (needTranslate[key]) {

                needTranslate[key]
                    .textContent = data[key];

            }

            cache[lang][key] =
                data[key];

        });

        setCache(cache);

        hideLoading();

    })

    .catch(err => {

        console.log(err);

        hideLoading();

    });

}